# Print Hello World.
print("Hello World")

# Add two numbers.
print(23.9 + 11.6)

myString <- "Hello, World!"
print(myString)

v <- TRUE
print(class(v)) # logical

v <- 23.5
print(class(v)) # numeric

v <- 2L        # we use L for the integer data type
print(class(v))

v <- "TRUE"    # character
print(class(v))

v <- charToRaw("Hello")  # raw ("Hello" is stored as 48 65 6c 6c 6f)
print(class(v))

